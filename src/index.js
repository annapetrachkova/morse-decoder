const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let res = '';
    for (let i = 0; i < expr.length; i += 10) {
        if (expr[i] === '*') {
            res += ' ';
        } else {
            let char = '';
            let size = i + 10;
            for (let j = i; j < size; j += 2) {
                if (expr[j] === '1' && expr[j + 1] === '1') {
                    char += '-';
                } else if (expr[j] === '1' && expr[j + 1] === '0') {
                    char += '.';
                }
            }
            res += MORSE_TABLE[char];
        }
    }
    return res;
}

module.exports = {
    decode
}